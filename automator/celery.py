from __future__ import absolute_import
import os
from celery import Celery

# set the default Django settings module for the 'celery' program.
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'automator.settings')

from django.conf import settings

os.environ.setdefault('ANSIBLE_CONFIG', '%s%s' % (settings.ANSIBLE['PATH'], settings.ANSIBLE['CONFIG_FILE']))
os.environ.setdefault('BROKER_URL', settings.BROKER_URL) #TODO: Add to sysconfig script for automatord
os.environ.setdefault('BACKEND_URL', settings.BACKEND_URL) #TODO: Add to sysconfig script automatord

app = Celery('automator', broker=settings.BROKER_URL, backend=settings.BACKEND_URL)

# Using a string here means the worker will not have to
# pickle the object when using Windows.
app.config_from_object('django.conf:settings')
app.autodiscover_tasks(lambda: settings.INSTALLED_APPS)

@app.task(bind=True)
def debug_task(self):
    print('Request: {0!r}'.format(self.request))


